bool View::loadIcon(const String& path)
{
    bool isSuccessful = false;

    if (false == m_bitmapWidget.load(FILESYSTEM, path))
    {
        LOG_WARNING("Failed to load icon %s.", path.c_str());
    }
    else
    {
        isSuccessful = true;
    }

    return isSuccessful;
}

